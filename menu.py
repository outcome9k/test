def run_menu():
    while True:
        print("\n== Tool Menu ==")
        print("1. Black2")
        print("2. Fix")
        print("3. Emo")
        print("4. Exit")

        choice = input("Choose option: ")
        if choice == '1':
            print("Running Black2...")
        elif choice == '2':
            print("Running Fix...")
        elif choice == '3':
            print("Running Emo...")
        elif choice == '4':
            print("Bye!")
            break
        else:
            print("Invalid option. Try again.")
